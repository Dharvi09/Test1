# Define a class
class Person
  attr_accessor :name, :age

  def initialize(name, age)
    @name = name
    @age = age
  end

  def greet
    puts "Hello, my name is #{@name} and I am #{@age} years old."
  end
end

# Create an instance of the Person class
person = Person.new("John Doe", 30)

# Call methods and access properties
person.greet
puts "Name: #{person.name}"
puts "Age: #{person.age}"

# Inheritance
class Student < Person
  attr_accessor :student_id

  def initialize(name, age, student_id)
    super(name, age)
    @student_id = student_id
  end

  def greet
    super
    puts "I am a student with ID: #{@student_id}"
  end
end

# Create an instance of the Student class
student = Student.new("Jane Smith", 25, "S12345")

# Call methods and access properties
student.greet
puts "Name: #{student.name}"
puts "Age: #{student.age}"
puts "Student ID: #{student.student_id}"