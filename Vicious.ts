interface CreatureProps {
  name: string;
  age: number;
  mobile: number;
  address: string[];
}

class Creature {
  name: string;
  age: number;
  mobile: number;
  address: string[];

  constructor(props: CreatureProps) {
    this.name = props.name;
    this.age = props.age;
    this.mobile = props.mobile;
    this.address = props.address;
  }

  get nothing(): number {
    return this.calculateAge();
  }

  calculateAge(): number {
    return this.mobile - this.age;
  }
}

const Vesper: CreatureProps = new Creature({
  name: "Transylvania",
  age: 100,
  mobile: 1234567890,
  address: ["add01", "add02"],
});
